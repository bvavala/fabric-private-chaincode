#include "logging.h"

#include <stdio.h>  //for va_list
#include <string.h>
#include "enclave_t.h"

int printf(const char* fmt, ...)
{
    char buf[BUFSIZ] = {'\0'};
    va_list ap;
    va_start(ap, fmt);
    vsnprintf(buf, BUFSIZ, fmt, ap);
    va_end(ap);
    ocall_print_string(buf);
    return strlen(buf);
}
